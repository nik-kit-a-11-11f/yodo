#!/bin/bash
# Скрипт резервного копирования для ТехноМир
# Этот скрипт сильно нагружает CPU и вызывает проблемы с веб-сервером

echo "$(date): Запуск резервного копирования ТехноМир..."

# Имитация тяжелой операции
for i in {1..100}; do
    echo "Копирование файла $i..." > /dev/null
    # Имитация обработки данных
    dd if=/dev/zero of=/tmp/backup_temp_$i bs=1M count=10 2>/dev/null
    sleep 1
done

echo "$(date): Резервное копирование завершено"
